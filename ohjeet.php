			<h1>Editorin ohjeet</h1>
				<p>
				<b>Kun haluat luoda uuden pelin:</b>
					<ol>
					<li> Kirjaa pelisi "Uuden pelin nimi" kohtaan ja paina "Luo tiedosto".</li>
					<li> Pelin nimi ilmestyy "Pelin nimi" kohtaa, jonka jälkeen voit lisätä "maaleja" kirjoittamalla ne "Lisättävä maali" kohtaan ja painamalla "Lisää maali" painiketta.</li>
					<li> Lisättyäsi tarpeeksi maaleja voit lisätä liikkuvat osat "Lisää liikkuva osa" painikkeesta.</li>
					<li> Kirjaa alle ilmestyviin kenttiin liikkuvan osan teksti ja valitse sille pari maaleista, jotka kirjasit aiemmin.</li>
					<li> Lopuksi paina "Upload / vie" painiketta ja esikatselu ikkuna avautuu.</li>
					<li> Esikatselun voit avata "Avaa peli" painikkeesta.
					<li> Esikatselun jälkeen voit antaa pelillesi nimen viereisessä osiossa ja painamalla "Vie peli etusivulle" pelisi ilmestyy etusivun pudotusvalikkoon.</li>
					</ol>
				
				<b>Kun haluat luoda kuvallisen pelin:</b>
					<ol>
					<li >Ensimmäisenä avaa kuvien lataus osuus painamalla "Kuvien lataus serverille" painiketta.</li>
					<li> Seuraavaksi valitse tiedostot painamalla saman nimistä painiketta ja valitsemalla haluamasi kuvat pitämällä CTRL nappia pohjassa ja painamalla kaikkia haluamiasi kuvia.</li>
					<li> Tuetut tiedosto muodot ovat: jpg, jpeg, png, bmp, tiff ja gif.</li>
					<li> Valittuasi kuvat luo uusi kansio nimeämällä se "Nimeä kuvien kansio" kohtaan ja painamalla Lataa Kuvat painiketta.</li>
					<li> Jos haluat lisätä tiettyyn kansioon kuvia jätä nimeämis osio tyhjäksi ja valitse pudotusvalikosta oikea kansio ja paina lataus nappia.</li>
					<li> Voit myös tarkastella mitä kansioissa on painamalla "avaa kuvien selaus" painiketta.</li>
					<li> Ladattuasi kuvat tee uusi peli samalla tavalla kuin normaalisti, mutta lisätessäsi uusia liikkuvia osia paina joko "Lisää kaikki kansion kuvat" painiketta lisätäksesi pudotusvalikon kuvat.</li>
					<li> Voit myös lisätä kuvia avaamalla kuvien lisäyksen saman nimisestä painikkeesta ja klikkaamalla kuvia lisäät ne liikkuvina osina peliin.</li>
					<li> Lisättyäsi kuvat paina "Upload / Vie" painiketta ja jatka samalla tavalla kuin normaalisti.</li>
					</ol>
				
				<b>Vanhojen pelien editointi:</b>
					<ol>
					<li> Paina "Editoi vanhaa peliä" painiketta editorissa siirtyäksesi tähän tilaan.</li>
					<li> Avaa pelisi kirjoittamalla sen nimi "Editoitava peli" osioon. Muista laittaa ".xml" pelisi nimen jälkeen.</li>
					<li> Avattuasi pelisi elementit tulevat esille ja voit muokata vastauksia, lisätä ja poistaa elementtejä ja maaleja.</li>
					<li> Editoimisen jälkeen paina "Tallenna editoimasi tiedosto" painiketta varmistaaksesi muutokset.</li>
					
					</ol>
				</p>
